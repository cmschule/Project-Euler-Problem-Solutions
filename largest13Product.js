function largest13Product(num) {
	var numString = String(num);
	var numArray = [];
	for (var i = 0; i < numString.length; i++) {
		numArray.push(Number(numString[i]));
	}
	var product;
	var maxProduct = 0;
	for (var j = 12; j < numArray.length; j++) {
		if (numArray[j]===0 && j < numArray.length-14) {
			j += 13;
		}
		product =1;
		for (var k = j-12; k <= j; k++) {
			product *= numArray[k];
		}
		if (product>maxProduct) {
			maxProduct = product;
		}
	}
	return maxProduct;

}

